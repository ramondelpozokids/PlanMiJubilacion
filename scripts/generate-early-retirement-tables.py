def matrix_to_by_month(matrix: dict[int, list[float]]) -> dict[str, dict[str, float]]:
    out: dict[str, dict[str, float]] = {}
    for month, vals in matrix.items():
        out[str(month)] = {BRACKETS[i]: vals[i] for i in range(4)}
    return out


def write_json(path: Path, data: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def build_dt34_for_year(year: int) -> dict[str, dict[str, float]]:
    idx = year - 2024
    out: dict[str, dict[str, float]] = {}
    for month in range(1, 25):
        out[str(month)] = {
            BRACKETS[0]: B0[month][idx],
            BRACKETS[1]: B1[month][idx],
            BRACKETS[2]: B2[month][idx],
            BRACKETS[3]: B3[month][idx],
        }
    return out


